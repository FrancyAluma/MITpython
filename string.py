# String, it's just a collection of caracters

text = "hello"
text2 = "THERE"

print(text)

# Accessing an element in a string
print(text[1])
print(text[4])

# Size of a string
print(len(text2))

# Modifying a string
print(text.upper())
print(text2.lower())

# String concatenation - combining or joining strings
print(text + text2)
print(text + " " + text2)